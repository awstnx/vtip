    """Определяет стоимость звонка в другой город по коду города и длительности вызова"""
    if city == 343:
        print('Стоимость звонка в Екатеринбург составляет: ', time*15, 'рублей')
    elif city == 381:
        print('Стоимость звонка Омск составляет: ', time*18, 'рублей')
    elif city == 473:
        print('Стоимость звонка в Воронеж составляет: ', time*13, 'рублей')
    elif city == 485:
        print('Стоимость звонка в Ярославль составляет: ', time*11, 'рублей')
    else:
        print('Код города не найден :c')
